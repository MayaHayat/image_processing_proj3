# --- 1. Custom Dataset Class ---
class Part2Dataset(Dataset):
    def __init__(self, root_dir, split='train', transform=None, augment=False):
        self.root = os.path.join(root_dir, split)
        if not os.path.exists(self.root):
            raise FileNotFoundError(f"Directory not found: {self.root}")
        self.img_files = sorted([f for f in os.listdir(self.root) if f.endswith('.jpg')])
        self.transform = transform
        self.augment = augment

    def __len__(self):
        return len(self.img_files)

    def __getitem__(self, idx):
        img_path = os.path.join(self.root, self.img_files[idx])
        xml_path = img_path.replace('.jpg', '.xml')

        image = Image.open(img_path).convert("RGB")
        w, h = image.size

        tree = ET.parse(xml_path)
        root = tree.getroot()
        box = root.find('object').find('bndbox')

        # Normalized coordinates (0 to 1)
        xmin = float(box.find('xmin').text) / w
        ymin = float(box.find('ymin').text) / h
        xmax = float(box.find('xmax').text) / w
        ymax = float(box.find('ymax').text) / h

        target = torch.tensor([xmin, ymin, xmax, ymax], dtype=torch.float32)

        # Training-time augmentation (before resize/normalize)
        if self.augment:
            # Random horizontal flip (update bbox to match)
            if random.random() < 0.5:
                image = TF.hflip(image)
                target = torch.tensor([1 - target[2], target[1], 1 - target[0], target[3]], dtype=torch.float32)
            # Random color jitter (no bbox change)
            if random.random() < 0.5:
                image = TF.adjust_brightness(image, random.uniform(0.8, 1.2))
            if random.random() < 0.5:
                image = TF.adjust_contrast(image, random.uniform(0.8, 1.2))
            if random.random() < 0.5:
                image = TF.adjust_saturation(image, random.uniform(0.8, 1.2))

        if self.transform:
            image = self.transform(image)
        return image, target

# --- 2. Model Architecture ---
class MobileNetDetector(nn.Module):
    def __init__(self):
        super().__init__()
        # Using pre-trained weights for transfer learning
        backbone = models.mobilenet_v3_large(weights='DEFAULT')
        self.features = backbone.features
        self.avgpool = nn.AdaptiveAvgPool2d(1)
        self.regressor = nn.Sequential(
            nn.Linear(960, 128),
            nn.ReLU(),
            nn.Linear(128, 4),
            nn.Sigmoid() # Sigmoid ensures output is between 0 and 1
        )

    def forward(self, x):
        x = self.features(x)
        x = self.avgpool(x)
        x = torch.flatten(x, 1)
        return self.regressor(x)

# --- 3. Utility Functions ---
def calculate_iou(preds, labels):
    x1, y1 = torch.max(preds[:, 0], labels[:, 0]), torch.max(preds[:, 1], labels[:, 1])
    x2, y2 = torch.min(preds[:, 2], labels[:, 2]), torch.min(preds[:, 3], labels[:, 3])
    intersection = (x2 - x1).clamp(0) * (y2 - y1).clamp(0)
    area_pred = (preds[:, 2] - preds[:, 0]) * (preds[:, 3] - preds[:, 1])
    area_gt = (labels[:, 2] - labels[:, 0]) * (labels[:, 3] - labels[:, 1])
    union = area_pred + area_gt - intersection + 1e-6
    return (intersection / union).mean()

def log_images_to_tb(writer, epoch, imgs, targets, preds, name="Val_Samples"):
    """Visualizes 4 images with Green (GT) and Red (Pred) boxes."""
    img_list = []
    num_to_log = min(4, imgs.shape[0])

    for i in range(num_to_log):
        # Denormalize image
        img = imgs[i].cpu().permute(1, 2, 0).numpy()
        img = img * [0.229, 0.224, 0.225] + [0.485, 0.456, 0.406]
        img = (img.clip(0, 1) * 255).astype('uint8').copy()
        h, w, _ = img.shape

        # Red Box: Predicted
        p = preds[i].cpu().numpy()
        cv2.rectangle(img, (int(p[0]*w), int(p[1]*h)), (int(p[2]*w), int(p[3]*h)), (255, 0, 0), 2)

        # Green Box: Ground Truth
        t = targets[i].cpu().numpy()
        cv2.rectangle(img, (int(t[0]*w), int(t[1]*h)), (int(t[2]*w), int(t[3]*h)), (0, 255, 0), 2)

        img_list.append(torch.from_numpy(img).permute(2, 0, 1))

    grid = vutils.make_grid(img_list)
    writer.add_image(name, grid, epoch)

