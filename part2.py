        val_loss, val_iou = 0, 0
        with torch.no_grad():
            for i, (imgs, targets) in enumerate(val_loader):
                imgs, targets = imgs.to(device), targets.to(device)
                preds = model(imgs)
                val_loss += criterion(preds, targets).item()
