def get_dimensions_df(root_path):
    data = []
    # VOC dataset typically uses these three splits
    splits = ['train', 'valid', 'test']

    for split in splits:
        split_path = os.path.join(root_path, split)
        if not os.path.exists(split_path):
            continue

        # List all XML files in the folder
        xml_files = [f for f in os.listdir(split_path) if f.endswith('.xml')]

        for f in xml_files:
            xml_p = os.path.join(split_path, f)
            tree = ET.parse(xml_p)
            root = tree.getroot()

            # Extract width and height from the <size> tag
            size = root.find('size')
            if size is not None:
                w = int(size.find('width').text)
                h = int(size.find('height').text)

                data.append({
                    'split': split,
                    'filename': f.replace('.xml', '.jpg'),
                    'width': w,
                    'height': h
                })

    return pd.DataFrame(data)

