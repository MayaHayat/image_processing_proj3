import matplotlib.pyplot as plt
import matplotlib.patches as patches

def plot_test_predictions(model, loader, device, num_images=16):
    model.eval()
    # Get one batch of data from the test set
    images, targets = next(iter(loader))
    images, targets = images.to(device), targets.to(device)

    with torch.no_grad():
        predictions = model(images)

    # Setup the plot grid
    rows = 4
    plt.figure(figsize=(18, 6 * rows))

    for i in range(num_images):
        # 1. Denormalize the image for display
        img = images[i].cpu().permute(1, 2, 0).numpy()
        # MobileNet normalization values
        img = img * [0.229, 0.224, 0.225] + [0.485, 0.456, 0.406]
        img = img.clip(0, 1)

        plt.subplot(rows, 4, i + 1)
        plt.imshow(img)

        # 2. Get coordinates (scaling 0-1 back to 224 pixels)
        pred_box = predictions[i].cpu().numpy() * 224
        gt_box = targets[i].cpu().numpy() * 224

        # 3. Create Rectangles
        # patches.Rectangle uses (x, y, width, height)
        rect_pred = patches.Rectangle((pred_box[0], pred_box[1]),
                                     pred_box[2] - pred_box[0],
                                     pred_box[3] - pred_box[1],
                                     linewidth=3, edgecolor='red', facecolor='none', label='Pred')

        rect_gt = patches.Rectangle((gt_box[0], gt_box[1]),
                                   gt_box[2] - gt_box[0],
                                   gt_box[3] - gt_box[1],
                                   linewidth=2, edgecolor='lime', facecolor='none', linestyle='--', label='GT')

        # 4. Add patches to the axes
        ax = plt.gca()
        ax.add_patch(rect_pred)
        ax.add_patch(rect_gt)

        # 5. Calculate individual IoU for the title
        iou_score = calculate_iou(predictions[i:i+1], targets[i:i+1]).item()
        plt.title(f"Test Image {i+1} | IoU: {iou_score:.2f}")
        plt.axis('off')

    plt.legend(loc='upper right')
    plt.tight_layout()
    plt.show()

# --- Run the Visualization ---
plot_test_predictions(model, test_loader, device)

